class Product {
  final String id;
  final String name;
  final double price;
  final List<String> images;
  final String details;
  final String description;
  final String category;

  Product({
    required this.id,
    required this.name,
    required this.price,
    required this.images,
    required this.details,
    required this.description,
    required this.category
  });
}
